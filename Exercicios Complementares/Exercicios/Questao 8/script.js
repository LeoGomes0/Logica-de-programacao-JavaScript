var numInformado1 = parseFloat(prompt("Informe o 1º número"));
var numInformado2 = parseFloat(prompt("Informe o 2º número"));
var numInformado3 = parseFloat(prompt("Informe o 3º número"));

// Calcular a média aritmética
var mediaAritmetica = (numInformado1 + numInformado2 + numInformado3) / 3;

// Exibir o resultado
document.write("A média aritmética é: " + mediaAritmetica);